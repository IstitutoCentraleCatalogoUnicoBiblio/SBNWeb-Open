package it.iccu.sbn.ejb.vo.acquisizioni.common;

import it.iccu.sbn.ejb.vo.SerializableVO;

public class StrutturaCombo  extends SerializableVO {
	/**
	 *
	 */
	private static final long serialVersionUID = -4814574284361226009L;

	private String codice;

	private String descrizione;
	public StrutturaCombo() throws Exception {
		codice ="";
		descrizione= "";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((codice == null) ? 0 : codice.hashCode());
		result = prime * result
				+ ((descrizione == null) ? 0 : descrizione.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		final StrutturaCombo other = (StrutturaCombo) obj;
		if (codice == null) {
			if (other.codice != null)
				return false;
		} else if (!codice.equals(other.codice))
			return false;
		if (descrizione == null) {
			if (other.descrizione != null)
				return false;
		} else if (!descrizione.equals(other.descrizione))
			return false;
		return true;
	}

	public StrutturaCombo(String cod, String des) throws Exception {
		if (cod == null) {
			throw new Exception("Codice non valido");
		}
		if (des == null) {
			throw new Exception("Descrizione non valida");
		}

		this.codice = cod;
		this.descrizione = des;
	}
	public String getCodice() {
		return codice;
	}

	public void setCodice(String codice) {
		this.codice = codice;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}
	public String getCoppia() {
		String coppia=getCodice()+ "|" + getDescrizione();
		return coppia;
	}
}
